.MODEL SMALL 
.STACK 100H 
.DATA

N DW ?
CR EQU 0DH
LF EQU 0AH 

ARR DD 1,3,10,12,4,6,2,9,7,11 
HIGHEST DB 0
SECOND_HIGHEST DB ?

.CODE    
MAIN PROC 
    MOV AX, @DATA
    MOV DS, AX   
    
    MOV CX,10 
    LEA SI,ARR
    
BEGIN_ARR:
    MOV AL,[SI] 
    MOV BL,HIGHEST
    CMP AL,BL
    JLE CONTINUE_LOOP
   
    MOV HIGHEST,AL  
    MOV BL,SECOND_HIGHEST
    CMP AL,BL
    JLE CONTINUE_LOOP
    MOV SECOND_HIGHEST,AL
    
CONTINUE_LOOP: 
    ADD SI,4
    LOOP BEGIN_ARR   

	; interrupt to exit
    MOV AH, 4CH
    INT 21H
MAIN ENDP         
END MAIN 

